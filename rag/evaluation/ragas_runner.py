"""
rag/evaluation/ragas_runner.py — Offline RAGAS evaluation with local LLM as judge.

Runs RAGAS faithfulness, answer_relevancy, and context_precision metrics entirely
locally. No data is sent to any external service.

Usage:
    from rag.config import load_config
    from rag.evaluation.test_generator import create_eval_dataset
    from rag.evaluation.ragas_runner import run_evaluation

    config = load_config()
    dataset = create_eval_dataset(config, n=20)
    results = run_evaluation(dataset, config, output_path=Path("results/ragas.json"))
    print(results)
"""
from __future__ import annotations

import json
import logging
from datetime import UTC, datetime
from pathlib import Path
from typing import TYPE_CHECKING

if TYPE_CHECKING:
    from rag.config import RAGConfig

log = logging.getLogger(__name__)


def run_evaluation(
    dataset: list[dict],
    config: RAGConfig,
    output_path: Path | None = None,
    metrics: list[str] | None = None,
) -> dict:
    """
    Run RAGAS evaluation on a dataset of question-answer pairs.

    Dataset format (from test_generator.py):
    [
        {
            "question": str,
            "ground_truth": str,       # The source chunk text
            "answer": str,             # Pipeline-generated answer (optional)
            "contexts": List[str],     # Retrieved passage texts used (optional)
            "source": str,
        },
        ...
    ]

    If "answer" and "contexts" are missing, they are generated by running
    the pipeline on each question first.

    Args:
        dataset:      List of eval dicts. Questions required; answers optional.
        config:       RAGConfig for LLM judge and pipeline.
        output_path:  Save JSON results to this path if provided.
        metrics:      Which RAGAS metrics to compute. Defaults to all three.
                      Options: "faithfulness", "answer_relevancy", "context_precision"

    Returns:
        Dict with keys: "faithfulness", "answer_relevancy", "context_precision",
        "n_evaluated", "timestamp"
    """
    try:
        import sys

        import langchain_community.chat_models
        class DummyVertexAI: pass
        langchain_community.chat_models.ChatVertexAI = DummyVertexAI  # type: ignore
        sys.modules['langchain_community.chat_models.vertexai'] = type('DummyMod', (), {'ChatVertexAI': DummyVertexAI})  # type: ignore

        from datasets import Dataset as HFDataset  # type: ignore[import]
        from ragas import evaluate  # type: ignore[import]
        from ragas.metrics import (  # type: ignore[import]
            answer_relevancy,
            context_precision,
            faithfulness,
        )
    except ImportError as exc:
        raise ImportError(
            "ragas and datasets are required. Run: pip install ragas datasets"
        ) from exc

    from rag.pipeline import QueryPipeline

    metrics_map = {
        "faithfulness": faithfulness,
        "answer_relevancy": answer_relevancy,
        "context_precision": context_precision,
    }
    selected_metrics = [metrics_map[m] for m in (metrics or list(metrics_map.keys()))]

    # Step 1: Generate answers + contexts for items that don't have them
    pipeline = QueryPipeline(config)
    enriched = []
    
    # Try to load cached generation results
    cache_file = Path("ragas_results_cache.json")
    cached_answers = {}
    if cache_file.exists():
        try:
            with open(str(cache_file), encoding="utf-8") as f:
                cached_data = json.load(f)
                cached_answers = {item["question"]: item for item in cached_data if "answer" in item}
            log.info("Loaded %d cached pipeline responses", len(cached_answers))
        except Exception as e:
            log.warning("Failed to load cache: %s", e)

    for item in dataset:
        q = item.get("question", "")
        if q in cached_answers:
            log.info("Using cached answer for: %.60s", q)
            enriched.append(cached_answers[q])
            continue
            
        if "answer" not in item or "contexts" not in item:
            log.info("Generating pipeline answer for: %.60s", q)
            try:
                result = pipeline.answer(query=q, history=[])
                item = dict(item)
                item["answer"] = result.text
                item["contexts"] = [c.excerpt for c in result.citations]
            except Exception as e:
                log.warning("Failed to run pipeline for question: %s", e)
                item = dict(item)
                item["answer"] = ""
                item["contexts"] = []
        enriched.append(item)
        
        # Save intermediate progress
        try:
            with open(str(cache_file), "w", encoding="utf-8") as f:
                json.dump(enriched, f, indent=2)
        except Exception:
            pass

    # Filter out items with empty answers
    valid = [e for e in enriched if e.get("answer")]
    
    # QUICK RUN: limit to 5 items to save time
    valid = valid[:5]
    
    if not valid:
        log.warning("No valid evaluated items — returning zero scores.")
        return {
            "faithfulness": 0.0,
            "answer_relevancy": 0.0,
            "context_precision": 0.0,
            "n_evaluated": 0,
            "timestamp": datetime.now(UTC).isoformat(),
        }

    # Step 2: Build Hugging Face Dataset
    hf_data = HFDataset.from_list([
        {
            "question": e["question"],
            "answer": e["answer"],
            "contexts": e.get("contexts") or [e.get("ground_truth", "")],
            "ground_truth": e.get("ground_truth", ""),
        }
        for e in valid
    ])

    # Step 3: Wrap local models for RAGAS
    llm_wrapper = _LocalLLMWrapper(config)
    embeddings_wrapper = _LocalEmbeddingsWrapper(config)

    # Step 4: Run RAGAS evaluation
    log.info("Running RAGAS evaluation on %d items...", len(valid))
    try:
        scores = evaluate(
            hf_data,
            metrics=selected_metrics,
            llm=llm_wrapper,
            embeddings=embeddings_wrapper,  # type: ignore[arg-type]
            raise_exceptions=False,
        )
    except Exception as e:
        log.error("RAGAS evaluation failed: %s", e)
        raise

    # EvaluationResult supports dict-style access in newer ragas versions.
    # Use a safe helper that handles both dict and attribute access.
    def _score(key: str) -> float:
        try:
            # ragas >= 0.1: EvaluationResult is dict-like
            val = scores[key]  # type: ignore[index]
        except (KeyError, TypeError):
            try:
                val = getattr(scores, key, None)
            except Exception:
                val = None
        # RAGAS may return a list[float] (per-sample scores) or a scalar.
        # In both cases we want the mean.
        if val is None:
            return 0.0
        if isinstance(val, (list, tuple)):
            return sum(val) / len(val) if val else 0.0
        return float(val)  # type: ignore[arg-type]

    results = {
        "faithfulness": _score("faithfulness"),
        "answer_relevancy": _score("answer_relevancy"),
        "context_precision": _score("context_precision"),
        "n_evaluated": len(valid),
        "timestamp": datetime.now(UTC).isoformat(),
    }

    if output_path:
        output_path.parent.mkdir(parents=True, exist_ok=True)
        with open(str(output_path), "w", encoding="utf-8") as f:
            json.dump(results, f, indent=2)
        log.info("RAGAS results saved to %s", output_path)

    return results


from typing import Any

from langchain_core.callbacks.manager import CallbackManagerForLLMRun
from langchain_core.language_models.llms import LLM


class _LocalLLMWrapper(LLM):
    """
    Thin adapter that wraps LLMClient for use as a RAGAS LLM judge.

    RAGAS expects a langchain-compatible LLM. We implement the minimal
    Langchain LLM interface so RAGAS can wrap it gracefully.
    """
    config: Any = None

    def __init__(self, config: RAGConfig, **kwargs: Any) -> None:
        super().__init__(**kwargs)
        self.config = config

    @property
    def _llm_type(self) -> str:
        return "local_llm_wrapper"

    def _call(
        self,
        prompt: str,
        stop: list[str] | None = None,
        run_manager: CallbackManagerForLLMRun | None = None,
        **kwargs: Any,
    ) -> str:
        """Synchronous inference call."""
        from rag.models.model_manager import get_model_manager
        try:
            llm = get_model_manager().get_llm(self.config)
            return llm.generate(prompt, max_tokens=200, temperature=0.1)
        except FileNotFoundError:
            log.warning("LLM not available for RAGAS judge — returning empty string.")
            return ""


from langchain_core.embeddings import Embeddings


class _LocalEmbeddingsWrapper(Embeddings):
    """
    Thin adapter that wraps Embedder for use in RAGAS.
    """

    def __init__(self, config: RAGConfig) -> None:
        self._config = config

    def embed_documents(self, texts: list[str]) -> list[list[float]]:
        from rag.models.model_manager import get_model_manager
        try:
            embedder = get_model_manager().get_embedder(self._config)
            # encode_batch returns (N, dim) np.ndarray
            embs = embedder.encode_batch(texts)
            return embs.tolist()
        except FileNotFoundError:
            log.warning("Embedder not available for RAGAS — returning zeros.")
            return [[0.0] * 768 for _ in texts]

    def embed_query(self, text: str) -> list[float]:
        return self.embed_documents([text])[0]

    async def aembed_documents(self, texts: list[str]) -> list[list[float]]:
        return self.embed_documents(texts)

    async def aembed_query(self, text: str) -> list[float]:
        return self.embed_query(text)



if __name__ == "__main__":
    import argparse
    logging.basicConfig(level=logging.INFO)
    from rag.config import load_config
    from rag.evaluation.test_generator import create_eval_dataset

    parser = argparse.ArgumentParser(description="Run RAGAS evaluation offline.")
    parser.add_argument("-n", type=int, default=20, help="Number of eval questions")
    parser.add_argument("-i", "--input", type=str, help="Input dataset JSON file")
    parser.add_argument("-o", "--output", type=str, default="results/ragas.json")
    args = parser.parse_args()

    cfg = load_config()
    
    if args.input:
        import json
        with open(args.input, encoding='utf-8') as f:
            ds = json.load(f)
        print(f"Loaded {len(ds)} questions from {args.input}")
    else:
        ds = create_eval_dataset(cfg, n=args.n)

    if not ds:
        print("No evaluation dataset could be generated. Ingest documents first.")
    else:
        res = run_evaluation(ds, cfg, output_path=Path(args.output))
        print("\n=== RAGAS Evaluation Results ===")
        for k, v in res.items():
            print(f"  {k}: {v}")
